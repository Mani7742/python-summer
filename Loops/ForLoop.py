# ------------------------------------------< For Loop >-------------------------------------------
# for loop is used to iterate over a sequence (list, tuple, string) or other iterable
a = "Abdul Rehman" #---> String
for i in a:
    print(i)

num = [1,2,3,4,5] #---> List
for val in num:
    print(val)

b = ("Abdul","Rehman","Shakeel") #---> Tuple
for i in b:
    print(i)